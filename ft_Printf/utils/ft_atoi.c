#include "ft_printf.h"

int	ft_atoi(const char *str)
{
	unsigned long	nb;
	int				i;
	int				s;

	i = 0;
	s = 1;
	nb = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i++] == '-')
			s = -s;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	if (nb > 2147483648 && s > 0)
		return (-1);
	if (nb > 2147483649 && s < 0)
		return (0);
	return ((int)(s * nb));
}
